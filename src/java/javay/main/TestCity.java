  class GoThread extends Thread {
    public void run() {
      for (int i = 0; i < panel.getComponentCount(); i ++) {
        component = panel.getComponent(i);
        //do something...
        go();
      }
      panel.repaint();
    }
  }

  private void go() {
    try {
      Thread.sleep(200);
    } catch (InterruptedException e) {
      e.printStackTrace();
    }
    SwingUtilities.invokeLater(run);//将对象排到事件派发线程的队列中
  }
